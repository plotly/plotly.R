    name = "Collaborate",
    icon = list(
      width = 1000,
      ascent = 500,
      descent = -50,
      path = sharingPath
    ),
    click = htmlwidgets::JS(sprintf(
      "function(gd) { 
        // is this being viewed in RStudio?
        if (location.search == '?viewer_pane=1') {
          alert('To learn about plotly for collaboration, visit:\\n %s');
        } else {
          window.open('%s', '_blank');
        }
      }", url, url))
  )
}

